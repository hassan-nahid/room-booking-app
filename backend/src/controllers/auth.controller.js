// @desc    Update user profile
// @route   PUT /api/auth/profile
// @access  Private
export const updateProfile = async (req, res) => {
  try {
    const allowedUpdates = [
      'firstName', 'lastName', 'phone', 'bio', 'dateOfBirth',
      'address', 'preferences', 'profileImage'
    ];
    
    const updates = {};
    Object.keys(req.body).forEach(key => {
      if (allowedUpdates.includes(key)) {
        updates[key] = req.body[key];
      }
    });

    const user = await User.findByIdAndUpdate(
      req.user.id,
      updates,
      { new: true, runValidators: true }
    );

    res.status(200).json({
      success: true,
      message: 'Profile updated successfully',
      data: { user }
    });
  } catch (error) {
    res.status(400).json({
      success: false,
      message: error.message
    });
  }
};

// @desc    Change password
// @route   PUT /api/auth/change-password
// @access  Private
export const changePassword = async (req, res) => {
  try {
    const { currentPassword, newPassword } = req.body;

    // Get user with password
    const user = await User.findById(req.user.id).select('+password');

    // Check current password
    const isCurrentPasswordValid = await user.comparePassword(currentPassword);
    if (!isCurrentPasswordValid) {
      return res.status(400).json({
        success: false,
        message: 'Current password is incorrect'
      });
    }

    // Update password
    user.password = newPassword;
    await user.save();

    res.status(200).json({
      success: true,
      message: 'Password changed successfully'
    });
  } catch (error) {
    res.status(400).json({
      success: false,
      message: error.message
    });
  }
};

// @desc    Become a host
// @route   POST /api/auth/become-host
// @access  Private
export const becomeHost = async (req, res) => {
  try {
    // Use lean query for faster initial check
    const existingHost = await User.findById(req.user.id).select('isHost').lean();
    
    if (existingHost.isHost) {
      return res.status(400).json({
        success: false,
        message: 'User is already a host'
      });
    }

    // Use atomic update for better performance
    const user = await User.findByIdAndUpdate(
      req.user.id,
      {
        $set: {
          isHost: true,
          role: 'host',
          'hostInfo.totalEarnings': 0,
          'hostInfo.totalBookings': 0,
          'hostInfo.averageRating': 0,
          'hostInfo.responseRate': 100,
          'hostInfo.responseTime': '< 1 hour',
          'hostInfo.joinedAsHostDate': new Date()
        }
      },
      { 
        new: true, 
        runValidators: true,
        select: '-password' // Don't return password
      }
    );

    res.status(200).json({
      success: true,
      message: 'Successfully became a host!',
      data: { user }
    });
  } catch (error) {
    res.status(400).json({
      success: false,
      message: error.message
    });
  }
};

